package String;

class a
{
	
	void sdata(int n , char w)
	{
		System.out.println(n+" "+w);
	}
	
	void spdata(char n , int k)
	{
		System.out.println(k+" "+n);
	}
	
}
public class same_name
{

	
	public static void main(String[] args) {
		
		
	a ss =  new a();
	ss.sdata(4, 's');
	ss.spdata('o',7);

	
		
	}
}
